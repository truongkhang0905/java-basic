package Day5;

public class Employee {
        int id;
        String name;
        String salary;
        String title;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "ID: " + this.getId() + "; Name: " + this.getName() + "; Salary: " + this.getSalary() + "; Title: " + this.getTitle();
    }
}
